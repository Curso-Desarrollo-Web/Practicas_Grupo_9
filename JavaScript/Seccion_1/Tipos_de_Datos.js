/******  TIPOS DE DATOS  ******/

//Numerico (entero, decimal)

// Enteros
100;
-100;
325;

// Decimales o flotantes
3.14;
25.65;
-56.78;



//String (CADENA DE CARACTERES)
"Diego Rodriguez";
'Diego Rodriguez';



//Booleano (Binary-data)
true;
false;
1;
0;


// Array
// Un array es una lista de elementos, estos pueden ser del mismo tipo o de diferentes tipos de dato.
[1,2,3,4,5];
["Diego", "Rodriguez"];
[true, false];
[1,"dos",3.14,true,"5"];


// Objetos
// Los objetos son una estructura de datos que nos permite almacenar información
// en pares clave/valor o key/value.

typeof{
    "nombre": "Diego", 
    "apellido": "Rodriguez"
};


// Undefined: Es un tipo de dato que se utiliza para indicar que no existe información, 
// es decir, que no tiene valor asignado.
undefined

// Null: Es un tipo de dato que se utiliza para indicar que no existe información.
null